/*
|--------------------------------------------------------------------------
| JS CLASICO
|--------------------------------------------------------------------------
*/

var array1 = [1, 2, 3];
var array2 = [4, 5, 6];
var union = array1.concat(array2);

console.log(union); // [1, 2, 3, 4, 5, 6]

/*
|--------------------------------------------------------------------------
| ECMAScript 
|--------------------------------------------------------------------------
*/

const array1 = [1, 2, 3];
const array2 = [4, 5, 6];
const union = [...array1, ...array2];

console.log(union); // [1, 2, 3, 4, 5, 6]